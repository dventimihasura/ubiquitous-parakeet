insert into "order" (account_id) with account as (
  select
    id,
    randUniform(0, 5)::int orders,
    name
    from
      account)
select
  account_id
  from (
    select
      account.id account_id,
      row_number() over (partition by account.id order by account.name) ordinal
      from account, numbers(0, 5) x) orders, account
 where  account.id = orders.account_id
   and orders.ordinal <= account.orders;

insert into order_detail (order_id, product_id, units)
with
  "order" as (
    select
      "order".id,
      (randUniform(0, 10))::int items
      from "order")
select
  order_id,
  product_id,
  (randUniform(0, 10))::int units
  from (
    select
      "order".id order_id,
      product.id product_id,
      row_number() over (partition by "order".id) ordinal
      from "order", product) user_item, "order"
 where "order".id = user_item.order_id
   and user_item.ordinal <= "order".items;
